int			ft_is_prime(int nb)
{
	int tmp;

	tmp = nb - 1;
	if (nb <= 1)
	{
		return (0);
	}
	while (tmp > 1)
	{
		if ((nb % tmp) == 0)
		{
			return (0);
		}
		else
		{
			tmp = tmp - 1;
		}
	}
	return (1);
}
